/**
 * 性別コードを日本語に変換
 */
export const genderToJapanese = (gender: string): string => {
  const genderMap: { [key: string]: string } = {
    'male': '男性',
    'female': '女性',
    'other': 'その他'
  };
  return genderMap[gender] || '未設定';
};
